package examples.other;

import java.util.*;
/**
 * Given two numbers, display a message saying:
 * "The maximum is X" (Whatever X is).
 */

public class ExerciseMax {

    public static void main(String[] args) {



        int x = 3;
        int y = 90;

        int max = y;


        if (x > y){
            max = y;



        }
        System.out.println("The maximum is: " + max);






    }
}
